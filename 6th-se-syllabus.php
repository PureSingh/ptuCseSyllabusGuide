<P class="p128 ft6">PREREQUISITIES :</P>
<P class="p327 ft5">Computer fundamentals, Concepts of structures programming, Programming in atleast on high level language, Elementary data structures, Elementary probability theory, Business Information system and Database Management System.</P>
<P class="p79 ft6">OBJECTIVES :</P>
<P class="p328 ft26">The course should provide an introduction to the fundamentals principles of software engineering. The present course should seek to equip the student with a repertoire of principles, tools and techniques and make him/her appreciate that software engineering is, after all, an exercise in making compromises.</P>
<P class="p138 ft6">COURSE CONTENTS</P>
<P class="p329 ft5"><SPAN class="ft6">Software Engineering Principles: </SPAN>How is software engineering an engineering discipline, Information system characteristics, software development process models, life cycle concepts, software phases and deliverables, software development strategies. [15 %]</P>
<P class="p330 ft5"><SPAN class="ft6">Technical Development: </SPAN>Structured systems analysis and design requirements collection and specification, data flow and logical data modeling, cost benefit analysis, feasibility study, architectural and detailed design, process, data, network, control and user interface designs, physical data design, dynamic modeling for <NOBR>real-time</NOBR> systems. [15 %]</P>
<P class="p331 ft26"><SPAN class="ft12">Software Project Management: </SPAN>principles of software project management organizational and team structure, project planning, project initiation and project termination; technical, quality and management plans, project controls, cost estimation <NOBR>methods-function</NOBR> points and COCOMO, tools. [15 %]</P>
<P class="p332 ft26"><SPAN class="ft12">Software Quality Management: </SPAN>quality control, quality assurance, quality standards, software metrics, verification and validation, testing, quality plans, tools Configuration Management [15 %]</P>
<P class="p333 ft26"><SPAN class="ft12">Software Development Method & CASE: </SPAN>formal, <NOBR>semi-formal</NOBR> and informal methods; data function, and <NOBR>event-based</NOBR> modeling, some of the popular methodologies such as Yourdon's SAD, SSADM etc. CASE tools, CASE standards.[20 %]</P>
