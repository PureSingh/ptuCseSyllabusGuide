<P class="p164 ft5"><SPAN class="ft6">PREREQUISITES: </SPAN>Calculus of two variables and exposure to <NOBR>mathematics-I</NOBR> and Mathematics - II.</P>
<P class="p79 ft6">OBJECTIVES:</P>
<P class="p79 ft5">To teach Engineering Mathematics to the students.</P>
<P class="p79 ft6">COURSE CONTENTS:</P>
<P class="p80 ft26">Review of the prerequisites such as limits of sequences and functions. Continuity, uniform continuity and differentiability. Rolls theorem, mean value theorems and Taylor's theorem. Newton method for approximate solution Riemann integral and the fundamental theorem of integral calculus. Approximate integration. Applications to length area, volume, surface area of revolution, Moments, centers of Mass and Gravity.</P>
<P class="p165 ft26">Repeated and multiple integrals with applications to volume, surface area, moments of inertia etc. Analytic functions, <NOBR>Cauchy-Riemann</NOBR> equations, Laplace equation, elementary functions, Cauchy's integral theorem(Proof by using Greens theorem), Cauchy's integral formula, Taylor series and Laurent series. [33%</P>
<P class="p166 ft5">Residues and applications to evaluating real improper integrals and inverse Laplace transforms. Conformal mapping, linear fractional transformations. [17%]</P>
<P class="p80 ft26">Boundary value problems involving partial differential equations such as wave equation, heat equation, Laplace equations . Solutions by the method of separation of variables and by Fourier and Laplace transforms. [33%]</P>
<P class="p102 ft5">Numerical Methods for ODEs and PDEs. [17%]</P>
