<P class="p119 ft12">PREREQUISITES:</P>
<P class="p111 ft26">The algorithms presented should be written in a pseudocode similar to the programming language Pascal / C and therefore, the readers should preferable be familiar with Pascal / C. The experience in structured programming and knowledge of combinatorial mathematics would be helpful.</P>
<P class="p120 ft12">OBJECTIVES:</P>
<P class="p112 ft5">The course should provide one with a fairly good concept of the fundamentals of data structures and also of the commonly occurring algorithms. The mathematical model of data is an abstract concept of data such as set, list or graph. To make it useful for problem solving the abstraction is made concrete by going into the data structure of the model- its implementation and associated algorithms. Given a data structure, quite frequently , several alternative algorithms exist for the same operation. Naturally, the question analyzing an algorithm to determine its performance in relation to the other alternatives becomes important. The course should present the general approach towards analyzing and evaluating algorithms and while presenting an algorithm, its analysis should also be included as and when required.</P>
